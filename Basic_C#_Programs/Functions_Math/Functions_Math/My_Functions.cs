using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Functions_Math
{
    public static class My_Functions
    {
        public static int PlusFive(int number)
        {
            number += 5;
            return number;
        }

        public static int MultiplyByTen(int number)
        {
            number *= 10;
            return number;
        }

        public static int DivideByFive(int number)
        {
            number /= 5;
            return number;
        }


    }
}
